function setLanguage(langCode) {
    if (!TRANSLATIONS[langCode]) langCode = 'en';

    // Update active state in selector if exists
    const selector = document.getElementById('langSelector');
    if (selector) selector.value = langCode;

    // Save language preference always
    localStorage.setItem('healthguard_lang', langCode);
    document.documentElement.lang = langCode;

    // Traverse DOM for data-i18n tags on ALL pages (Instant Update)
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (!TRANSLATIONS[langCode] || !TRANSLATIONS[langCode][key]) return;

        if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
            el.setAttribute('placeholder', TRANSLATIONS[langCode][key]);
        } else if (el.tagName === 'OPTION') {
            el.textContent = TRANSLATIONS[langCode][key];
        } else {
            // Preservation logic for icons/wrappers if they exist as children
            const icon = el.querySelector('span, div, i, svg'); // Look for child icons
            if (icon && el.childNodes.length > 1) {
                // Keep the icon, update only the text node
                el.childNodes.forEach(node => {
                    if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
                        node.textContent = TRANSLATIONS[langCode][key];
                    }
                });
            } else {
                el.textContent = TRANSLATIONS[langCode][key];
            }
        }
    });

    // Dispatch global event for interactive JS components
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: langCode } }));

    if (typeof window._onLanguageChanged === 'function') {
        try { window._onLanguageChanged(langCode); } catch (err) { console.error('[i18n] callback error:', err); }
    }
}

// Initialize script on load
window.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('healthguard_lang') || 'en';
    setLanguage(savedLang);
});

