/**
 * Global Symptom Translation Retriever
 * Called by app.js to fetch the selected language string.
 */
function getSymptomLabel(id, isCategory = false) {
    const lang = localStorage.getItem('healthguard_lang') || 'en';
    if (typeof SYMPTOM_TRANSLATIONS !== 'undefined') {
        const entry = SYMPTOM_TRANSLATIONS[id];
        if (entry && entry[lang]) {
            return entry[lang];
        }
    }

    // Fallback to English from the original arrays inside app.js if they exist globally
    if (typeof SYMPTOMS !== 'undefined' && typeof SYMPTOM_CATEGORIES !== 'undefined') {
        if (isCategory) {
            const baseId = id.replace('cat_', '');
            const cat = SYMPTOM_CATEGORIES.find(c => c.id === baseId);
            return cat ? cat.label : id;
        } else {
            const sym = SYMPTOMS.find(s => s.id === id);
            return sym ? sym.label : id;
        }
    }

    return id; // Ultimate Fallback
}
