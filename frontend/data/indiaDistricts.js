// Deduplicate and sort
const UNIQUE_DISTRICTS = [...new Set(INDIA_DISTRICTS)].sort();

/**
 * Search districts with fuzzy matching
 * @param {string} query - Search term (3+ characters)
 * @param {number} limit - Max results (default: 10)
 * @returns {array} Matching districts
 */
function searchDistricts(query, limit = 10) {
  if (!query || query.length < 1) return [];

  const q = query.toLowerCase();

  // Exact prefix match (highest priority)
  let results = UNIQUE_DISTRICTS.filter(d => d.toLowerCase().startsWith(q));

  // If not enough, add word boundary matches
  if (results.length < limit) {
    const regex = new RegExp(`\\b${q}`, 'i');
    const wordMatches = UNIQUE_DISTRICTS.filter(d => regex.test(d) && !results.includes(d));
    results.push(...wordMatches);
  }

  // If still not enough, add substring matches (Levenshtein distance tolerance)
 results = results.slice(0, limit);
  return results;
}

